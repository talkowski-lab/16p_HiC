# Plot Matrix QC stuff
plot_qc_barplot <- function(
    plot.df,
    y_val,
    facet_row,
    facet_col,
    yintercept=35,
    ...){
    plot.df %>%
    ggplot(
        aes(
            x=Category,
            y=.data[[y_val]],
            fill=Sample.ID
        )
    ) +
    geom_col(position = "dodge") +
    geom_hline(yintercept=yintercept, color='black', linetype='dashed') +
    # scale_y_continuous(breaks=~ seq(.x, .y, 5)) +
    scale_y_continuous(
        breaks=seq(0, max(plot.df[[y_val]]), 5),
        expand=c(0, 0)
    ) +
    facet_grid2(
        rows=vars(!!sym(facet_row)),
        cols=vars(!!sym(facet_col)),
        scales='fixed'
    ) +
    labs(y=y_val) +
    theme(
        ...,
        axis.title.x=element_blank(),
        axis.text.x=
            element_text(
                hjust=1,
                angle=35
            )
    ) + 
    add_ggtheme()
}

